public class Operator2 {
	public static void main(String[] args) {
		/* 관계연산자(비교연산자)-> true, false
		 * a > b, a >= b
		 * a < b, a <= b
		 * a == b
		 * a != b
		 */
		System.out.println(2 > 3);
		System.out.println(2 < 3);
		System.out.println(2 <= 3);
		System.out.println(2 == 2);
		System.out.println(2 != 3);
	}

}
